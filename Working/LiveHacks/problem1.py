'''
Name: fahrenheit into celsius machine

Purpose:
 This code converts celsius into fahrenheit for Jeremy Lin.

Author: Andrew Jiang

Date: Wednesday October 2nd 2019
'''
# get celsius from user
celsius = float(input("Enter the celsius here: "))

# compute fahrenheit from celsius
fahrenheit = (9/5 * celsius) + 32

# output fahrenheit
print("The temperature in fahrenheit is:" + str(fahrenheit))
